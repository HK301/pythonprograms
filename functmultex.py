def multiplication(x,y):
    z = x*y
    return z
a = multiplication(2,3)
b = multiplication(5,6)
print(a,b)


def division(x,y):
    z = x/y
    return z
p = division(8,4)
q = division(16,4)
print(p,q)

def subtraction(x,y):
    z = x-y
    return z
s = subtraction(9,5)
t = subtraction(100,50)
print(s,t)

def areaofcircle(r):
    area = 3.1415*r*r
    return area
a = areaofcircle(5)
b = areaofcircle(10)
print(a,b)


def areaofrectangle(l,b):
    area = l*b
    return area
a = areaofrectangle(2,3)
b = areaofrectangle(5,6)
print(a,b)


def areaofsquare(s):
    area = s*s
    return area
